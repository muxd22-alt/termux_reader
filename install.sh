try {
  module.exports = require("./$REL_NODE");
} catch (e) {
  try {
    module.exports = require("../$REL_NODE");
  } catch (err) {
    console.error("\n[Termux Error] Could not load native binding: $REL_NODE");
    console.error("Path attempted: " + __dirname);
    process.exit(1);
  }
}
JSBOOT
        FOUND_LIBS=1
    fi
done

# 5c. Emergency Symlink Fallback
if [ "$FOUND_LIBS" -eq 0 ]; then
    warn "Primary bypass failed. Attempting symlink redirect..."
    mkdir -p "node_modules/@libsql"
    # Link whatever was built (linux-arm64-gnu) to the name Node/Android expects
    COMPANION=$(find node_modules/@libsql -name "linux-*" -type d | head -1 || echo "")
    if [ -n "$COMPANION" ]; then
        ln -sf "$(basename "$COMPANION")" "node_modules/@libsql/android-$LIB_ARCH"
        ok "Symlinked $(basename "$COMPANION") -> android-$LIB_ARCH"
    fi
fi

# 5d. Runtime Intercept (The "Safety Net")
if [ -f "server/index.ts" ]; then
    if ! grep -q "Module.prototype.require" "server/index.ts"; then
        # Safer injection: uses a temporary file to ensure we don't mangle the code
        echo 'import Module from "node:module";
// @ts-ignore
