// @ts-ignore
Module.prototype.require = function(id) {
  if (id.includes("android")) return origRequire.call(this, id.replace("android", "linux"));
  return origRequire.apply(this, arguments);
};' > inject.tmp
        cat server/index.ts >> inject.tmp
        mv inject.tmp server/index.ts
    fi
fi
ok "Bypass stack applied"

# --- 6. Build Frontend ---
step "Building frontend..."
npm run build 2>&1 | tail -n 5 || warn "Frontend build failed/skipped."

# --- 7. Final Reader Command ---
mkdir -p "$PREFIX/bin"
cat > "$PREFIX/bin/reader" << 'READEREOF'
