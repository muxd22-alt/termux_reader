R='\033[0;31m'; G='\033[0;32m'; Y='\033[1;33m'; C='\033[0;36m'; B='\033[1;34m'; NC='\033[0m'
DIM='\033[0;2m'

banner() {
    clear
    echo -e "${B}╔═══════════════════════════════════════════╗${NC}"
    echo -e "${B}║    ${G}📖  Oksskolten Termux Reader  📖${NC}    ${B}║${NC}"
    echo -e "${B}║       Native Android · Zero Overhead      ║${NC}"
    echo -e "${B}╚═══════════════════════════════════════════╝${NC}"
}

step() { echo -e "\n${B}[▸]${NC} $1"; }
ok()   { echo -e "  ${G}[✓]${NC} $1"; }
warn() { echo -e "  ${Y}[!]${NC} $1"; }
err()  { echo -e "  ${R}[✗]${NC} $1"; exit 1; }

banner

# --- 1. Environment ---
step "Ensuring native environment..."
pkg update -y
pkg install -y nodejs-lts git curl python make clang binutils pkg-config libsqlite openssl rust termux-elf-cleaner
ok "Toolchain ready"

# --- 2. Source Management ---
INSTALL_DIR="$HOME/oksskolten"
step "Fetching source..."
if [ -d "$INSTALL_DIR/.git" ]; then
    cd "$INSTALL_DIR" && git pull --ff-only
else
    git clone https://github.com/muxd22-alt/termux_reader.git "$INSTALL_DIR"
    cd "$INSTALL_DIR"
fi

# --- 3. Platform Forcing ---
step "Patching configs..."
# Patch package files to allow 'android' as an OS
find . -maxdepth 2 -name "package*.json" -exec sed -i 's/"os": \[/"os": \["android", /g' {} +
# Wipe engine restrictions
sed -i '/"engines": {/,/}/d' package.json 2>/dev/null || true
ok "Manifests patched"

# --- 4. Native Build ---
step "Building native modules (LibSQL/SQLite)..."
# Setting flags to ensure we use Termux's libraries
export LDFLAGS="-L$PREFIX/lib"
export CPPFLAGS="-I$PREFIX/include"
# Force local compilation
npm install --no-fund --no-audit --build-from-source || {
    warn "Direct build failed, attempting ignore-scripts fallback..."
    npm install --no-fund --no-audit --ignore-scripts
}

# --- 5. The "Unstoppable" Bypass ---
step "Applying Surgical Native Bypass..."

# Identify Architecture
ARCH=$(uname -m)
[ "$ARCH" == "aarch64" ] && LIB_ARCH="arm64" || LIB_ARCH="arm"

# 5a. Find and clean ALL .node files
find node_modules -name "*.node" | while read -r NODE_FILE; do
    echo -e "  ${DIM}Cleaning ELF headers: $(basename "$NODE_FILE")${NC}"
    termux-elf-cleaner "$NODE_FILE" > /dev/null 2>&1 || true
done

# 5b. Overwrite LibSQL Loaders
# This target index.js files inside @libsql and replaces them with a hard-coded redirect
FOUND_LIBS=0
find node_modules/@libsql -name "index.js" 2>/dev/null | while read -r LIBJS; do
    DIR=$(dirname "$LIBJS")
    # Check if there is a .node file in this directory or nearby
    NODE_FILE=$(find "$DIR" -name "*.node" | head -1)
    
    if [ -n "$NODE_FILE" ]; then
        REL_NODE=$(basename "$NODE_FILE")
        echo -e "  ${C}Surgically overriding: $LIBJS${NC}"
        cat > "$LIBJS" << JSBOOT
